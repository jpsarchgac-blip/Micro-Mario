# sprites.py - 全スプライトのビットマップデータ
#
# データ形式: MONO_VLSB(Vertical, LSB first)
#   1バイト = 縦8ピクセル、LSB=上、MSB=下
#   8x8スプライト=8バイト、8x16=16バイト、16x16=32バイト
#
# これは framebuf.FrameBuffer の MONO_VLSB と互換なので、
# 起動時に FrameBuffer に包んで blit() するだけで爆速描画できる。
#
# 描き方: 縦8列を上から見て、点灯ビットを下から積む
# 例: 上から「黒黒白白白白黒黒」なら 0b00111100 = 0x3C

# ========== プレイヤー(SMALL) 8x8 ==========
# MONO_VLSB: 1バイト=縦8px、LSB=一番上
# 列0(左)→列7(右)の順で8バイト
#
# チビマリオ 右向き:
#  col:  0     1     2     3     4     5     6     7
#  bit0  .     .     .     H     H     H     .     .   ← 帽子
#  bit1  .     H     H     H     H     H     H     .   ← 帽子つば
#  bit2  .     S     S     E     .     E     S     .   ← 肌・目
#  bit3  .     B     B     B     B     B     B     .   ← 胴体
#  bit4  O     O     O     O     O     O     O     O   ← ズボン
#  bit5  O     O     O     O     O     O     O     O   ← ズボン
#  bit6  .     L     .     .     .     .     R     .   ← 脚
#  bit7  .     L     .     .     .     .     R     .   ← 脚
# H=帽子 S=肌 E=目(白抜き) B=胴体 O=ズボン L=左脚 R=右脚
# 右向き立ち
# MONO_VLSB: bit0=最上行、bit7=最下行
# row0: 帽子(col2-5に帽子), row1: 帽子つば, row2: 顔・目
# row3-4: オーバーオール, row5-6: 脚
#
#       col0  col1  col2  col3  col4  col5  col6  col7
# row0   .     .     H     H     H     H     .     .   帽子
# row1   .     H     H     H     H     H     H     .   帽子つば
# row2   .     S     .     E     S     E     S     .   肌・目
# row3   .     B     B     B     B     B     B     .   胴上
# row4   P     P     P     P     P     P     P     P   ズボン
# row5   .     .     L     .     .     R     .     .   脚
# row6   .     .     L     .     .     R     .     .   脚
# row7   .     .     .     .     .     .     .     .   (余白)
