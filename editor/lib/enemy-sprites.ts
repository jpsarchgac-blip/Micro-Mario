// 8x8 MONO_VLSB sprites for enemies (mirrors sprites.py simplified)
import { EnemyType } from "./enemies";

const GOOMBA = [0x3C, 0x76, 0xFF, 0xDB, 0xDB, 0xFF, 0x76, 0x3C];
const GOOMBA_SQ = [0x00, 0x00, 0x38, 0x7E, 0x7E, 0x38, 0x00, 0x00];
const PATA = [0x18, 0x3C, 0x7E, 0x99, 0xBD, 0x66, 0x3C, 0x18];
const BAT = [0x42, 0x66, 0x7E, 0xFF, 0xFF, 0x7E, 0x66, 0x42];
const FISH = [0x10, 0x38, 0x7C, 0xFA, 0x7C, 0x38, 0x10, 0x10];
const KILLER = [0xFF, 0x81, 0xC3, 0x99, 0xBD, 0xC3, 0x81, 0xFF];
const BIG_MUSH = [0x3C, 0x7E, 0xFF, 0xDB, 0xDB, 0xFF, 0x7E, 0x3C];
const BOSS = [0xFF, 0xC3, 0xA5, 0x99, 0x99, 0xA5, 0xC3, 0xFF];

export function spriteFor(type: EnemyType, squashed: boolean, animTick = 0): number[] {
  switch (type) {
    case "goomba": return squashed ? GOOMBA_SQ : GOOMBA;
    case "pata_new": return PATA;
    case "bat": return (animTick >> 2) & 1 ? BAT.map((b) => (b << 1) & 0xff) : BAT;
    case "fish": return FISH;
    case "killer": return KILLER;
    case "killer_spawn": return KILLER; // not really drawn
    case "big_mushroom": return BIG_MUSH;
    case "boss": return BOSS; // single 8x8 placeholder
    default: return GOOMBA;
  }
}
