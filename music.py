# ========== SFX ==========
# 各SFX = リスト of (freq, dur_ms)。途中で freq=0 は休符。
# ジャンプ系は周波数スイープを階段近似。

def _sweep(f_start, f_end, steps, total_ms):
    """周波数スイープを階段で生成。"""
    out = []
    dur = max(1, total_ms // steps)
    for i in range(steps):
        f = f_start + (f_end - f_start) * i // steps
        out.append((f, dur))
    return out

